/**
 * Employee Display Configuration
 * 
 * Centralized source of truth for employee visual branding, stats, and display metadata.
 * Used by hero cards, chat headers, and employee-specific UI components.
 * 
 * This config is separate from employeeChatConfig.ts (which handles chat behavior)
 * and focuses purely on visual presentation and display properties.
 */

export interface EmployeeStat {
  value: string;
  label: string;
  colorClass: string; // Tailwind text color class, e.g. 'text-cyan-400'
}

export interface EmployeeDisplayConfig {
  emoji: string;
  displayName: string; // Short name, e.g. "Prime", "Byte"
  title: string; // Full title, e.g. "Prime ‚Äî AI Command Center"
  subtitle: string; // Short description
  gradient: string; // Tailwind gradient classes for buttons/accents
  accentGlow: string; // Tailwind class for radial glow behind avatar, e.g. 'bg-sky-500/10'
  accentShadow: string; // Tailwind shadow class for avatar glow, e.g. 'shadow-[0_0_40px_rgba(56,189,248,0.7)]'
  accentColor: string; // Primary accent color for borders/glow, e.g. 'sky-500'
  primaryButtonGradient: string; // Gradient for primary chat button
  primaryButtonShadow: string; // Shadow class for primary button, e.g. 'shadow-blue-900/60'
  stats?: EmployeeStat[]; // Default stats to display (optional, can be overridden)
  // Chat-specific display config
  chatTitle: string; // Short title for chat header, e.g. "Byte ‚Äî Chat"
  chatSubtitle: string; // One-line role description for chat header, e.g. "Smart Import & OCR Wizard"
  chatQuickPrompts: string[]; // Array of 3-6 suggested questions for "Try asking" row
  chatAccentGlow?: string; // Optional Tailwind class for avatar/glow ring in chat header (reuses accentGlow if not provided)
}

